//! # c05-structs - Rust 结构体学习模块
//!
//! 本模块包含 Rust 结构体与方法：
//!
//! - `c05-what-is-struct` - 什么是结构体
//! - `c05-impl-block` - impl 块与方法
//! - `c05-associated-fn` - 关联函数（理解 String::from）
//!
//! ## 运行方式
//!
//! ```bash
//! cargo run -p c05-structs --bin c05-what-is-struct
//! cargo run -p c05-structs --bin c05-impl-block
//! cargo run -p c05-structs --bin c05-associated-fn
//! ```
